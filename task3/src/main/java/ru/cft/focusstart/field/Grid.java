package ru.cft.focusstart.field;

public class Grid {
    private int gridX;
    private int gridY;

    Grid(int gridX, int gridY) {
        this.gridX = gridX;
        this.gridY = gridY;
    }

    public int getGridX() {
        return gridX;
    }

    public int getGridY() {
        return gridY;
    }
}
